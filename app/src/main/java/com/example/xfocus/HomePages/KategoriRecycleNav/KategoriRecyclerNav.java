package com.example.xfocus.HomePages.KategoriRecycleNav;

public class KategoriRecyclerNav {
    private int image;
    private String text;

    public KategoriRecyclerNav(int image, String text) {
        this.image = image;
        this.text = text;
    }

    public int getImage() {
        return image;
    }

    public String getText() {
        return text;
    }
}
